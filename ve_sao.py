import turtle
tl= turtle.Turtle()
tl.shape('turtle')
tl.speed(1)
tl.fillcolor('blue')
tl.begin_fill()
tl.forward(200)
tl.left(-144)
tl.forward(200)
tl.left(-144)
tl.forward(200)
tl.left(-144)
tl.forward(200)
tl.left(-144)
tl.forward(200)
tl.end_fill()
turtle.done()


